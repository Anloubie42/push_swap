#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*list;

	if (!(list = (t_list *)malloc(sizeof(t_list))))
		return (list);
	list->content = (void *)content;
	list->next = NULL;
	return (list);
}
